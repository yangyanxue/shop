package model;

public class Goods {
	int id;
	String name;
	int orignal_price;
	int now_price;
	int store_num;
	int item_id;
	String images;
	String description;
	String size;
	String color;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getOrignal_price() {
		return orignal_price;
	}
	public void setOrignal_price(int orignalPrice) {
		orignal_price = orignalPrice;
	}
	public int getNow_price() {
		return now_price;
	}
	public void setNow_price(int nowPrice) {
		now_price = nowPrice;
	}
	public int getStore_num() {
		return store_num;
	}
	public void setStore_num(int storeNum) {
		store_num = storeNum;
	}
	public int getItem_id() {
		return item_id;
	}
	public void setItem_id(int item_id) {
		this.item_id = item_id;
	}
	public String getImages() {
		return images;
	}
	public void setImages(String images) {
		this.images = images;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getSize() {
		return size;
	}
	public void setSize(String size) {
		this.size = size;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	
	

}
